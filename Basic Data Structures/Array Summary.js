
let greetings = ['whats up?', 'hello', 'see ya!'];
// remove last item of array, now equals ['whats up?', 'hello']
greetings.pop(); 

//  remove first item of array now equals ['hello']
greetings.shift(); 

//  push a item after last item of array, now equals ['hello','Frank'] , return new-length
greetings.push('Frank'); 

//  unshift a item before first item of array, now equals ['say','hello','Frank'], return new-length
greetings.unshift('say'); 

